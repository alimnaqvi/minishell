#include "minishell.h"

int	ft_isspace(char c)
{
	return (c == '\t' || c == '\v' || \
		c == '\f' || c == '\r' || c == ' ');
}

int	ft_isquote(char c)
{
	return (c == '\'' || c == '"');
}
